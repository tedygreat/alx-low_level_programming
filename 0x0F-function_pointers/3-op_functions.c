#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - function that perform
 * @a: var
 * @b: var2
 * Return: sumof a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that perform
 * @a: var
 * @b: var2
 * Return: of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that perform
 * @a: var
 * @b: var2
 * Return: of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that perform
 * @a: var
 * @b: var2
 * Return: mod of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that perform
 * @a: var
 * @b: var2
 * Return: mod of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
